'''
Write a program that displays the area and
perimeter of a rectangle with the width of 4.5 and height of 7.9 using the following
formula:
'''
import math
Width = 4.5
Height = 7.9
# perimeter of a rectangle formula
perimeter = 2*(Width*Height)
# area of a rectangle
area = Width*Height
# printing area and rectangle
print("The perimeter of rectangle is:", round(perimeter, 2))
print("The area of rectangle is:", round(area, 2))
